class Array2d {
    public static void main(String[] args) {
        int[][] b = {
            {1, 2, 3}, 
            {4, 5, 6}, 
            {7,8}, 
        };
        System.out.println("Length of row 1: " + b[0].length);
        System.out.println("Length of row 2: " + b[1].length);
        System.out.println("Length of row 3: " + b[2].length);
    }
}
